import { model, Schema } from "mongoose";

const userSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    isavailable: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);
export const captainModel = new model("Captains", userSchema);
